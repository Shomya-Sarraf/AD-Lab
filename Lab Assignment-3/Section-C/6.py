# 6. Create a boxplot for visualizing distribution of “monthly expenses”.
import matplotlib.pyplot as plt
expenses = [12000, 15000, 13000, 17000, 14000, 16000, 20000, 18000]
plt.boxplot(expenses)
plt.ylabel("Expenses")
plt.title("Monthly Expenses Distribution")
plt.show()
