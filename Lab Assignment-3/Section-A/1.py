# 1. Create a NumPy array of shape (5,5) with random integers between 10 and 50. Print max, min, and mean.
import numpy as np
arr = np.random.randint(10, 51, size=(5, 5))
print("Array:\n", arr)
print("Max:", arr.max())
print("Min:", arr.min())
print("Mean:", arr.mean())
